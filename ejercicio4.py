# Definir cuatro variables, llamadas a, b, c y d.
# Realizar la suma (a+b), resta (d-c), multiplicación (a*b*c*d) y división (a/b),
# y mostrar los resultados por pantalla, de forma que se vea qué operación es.
print("Escribe 4 números")
a = int(input())
b = int(input())
c = int(input())
d = int(input())
print("A =", a)
print("B =", b)
print("C =", c)
print("D =", d)
print("-----------------------------------------------------------")
suma = a + b
resta = d - c
multiplicacion = a * b * c * d
division = a / b
print("A + B =", suma)
print("D - C =", resta)
print("A * B * C * D =", multiplicacion)
print("A / B =", division)
print("")
